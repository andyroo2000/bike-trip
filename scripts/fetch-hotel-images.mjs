#!/usr/bin/env node
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUTPUT_DIR = path.join(__dirname, '../public/images/hotels');
const DATA_OUT  = path.join(__dirname, '../src/data/hotel-images.ts');

const API_KEY = process.env.PUBLIC_GOOGLE_MAPS_API_KEY;
if (!API_KEY) {
  console.error('ERROR: PUBLIC_GOOGLE_MAPS_API_KEY not set.');
  process.exit(1);
}

const hotels = [
  { slug: 'dormy-inn-sapporo',  query: 'Dormy Inn Sapporo Annex' },
  { slug: 'hotel-kumoi',        query: 'Hotel Kumoi Sounkyo Onsen Kamikawa Hokkaido' },
  { slug: 'oehonke',            query: 'Oehonke Onneyu Onsen Rubeshibe Kitami Hokkaido' },
  { slug: 'dormy-inn-abashiri', query: 'ドーミーイン網走' },
  { slug: 'shiretoko-yuhi',     query: 'Shiretoko Yuhi no Ataruie Onsen Hostel Utoro Shari Hokkaido' },
  { slug: 'livemax-rausu',      query: 'Livemax Resort Rausu' },
  { slug: 'bps-hotels',         query: 'BPS HOTELS Betsukai Hokkaido' },
  { slug: 'hotel-mashu',        query: 'ホテルまあしゅ 弟子屈' },
  { slug: 'new-akan-hotel',     query: 'New Akan Hotel' },
  { slug: 'hotel-reus-ashoro',  query: 'Hotel Reus Ashoro' },
  { slug: 'nukabira-kanko',     query: 'Nukabirakan Kanko Hotel' },
  { slug: 'sounkyo-kanko',      query: 'Sounkyo Kankou Hotel Sounkyo Onsen Kamikawa Hokkaido' },
];

async function fetchJson(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`HTTP ${res.status}`);
  return res.json();
}

async function downloadImage(url, destPath) {
  if (fs.existsSync(destPath)) return false; // already downloaded
  const res = await fetch(url);
  if (!res.ok) throw new Error(`HTTP ${res.status} downloading image`);
  const buf = await res.arrayBuffer();
  fs.writeFileSync(destPath, Buffer.from(buf));
  return true;
}

async function fetchHotelImages(hotel) {
  const hotelDir = path.join(OUTPUT_DIR, hotel.slug);
  fs.mkdirSync(hotelDir, { recursive: true });

  // Step 1: Text search
  const searchUrl = `https://maps.googleapis.com/maps/api/place/textsearch/json?query=${encodeURIComponent(hotel.query)}&key=${API_KEY}`;
  const searchData = await fetchJson(searchUrl);
  if (!searchData.results?.length) {
    console.error(`  [fail] ${hotel.slug} — no search results`);
    return [];
  }

  const placeId = searchData.results[0].place_id;
  const placeName = searchData.results[0].name;
  console.log(`  [found] ${hotel.slug} → "${placeName}"`);

  // Step 2: Place details — get all photos (Places API returns up to 10)
  const detailsUrl = `https://maps.googleapis.com/maps/api/place/details/json?place_id=${placeId}&fields=photos&key=${API_KEY}`;
  const detailsData = await fetchJson(detailsUrl);
  const photos = detailsData.result?.photos;
  if (!photos?.length) {
    console.error(`  [fail] ${hotel.slug} — no photos available`);
    return [];
  }

  // Step 3: Download each photo
  const paths = [];
  for (let i = 0; i < photos.length; i++) {
    const destPath = path.join(hotelDir, `${i + 1}.jpg`);
    const photoUrl = `https://maps.googleapis.com/maps/api/place/photo?maxwidth=1200&photoreference=${photos[i].photo_reference}&key=${API_KEY}`;
    const downloaded = await downloadImage(photoUrl, destPath);
    const webPath = `/images/hotels/${hotel.slug}/${i + 1}.jpg`;
    paths.push(webPath);
    console.log(`  ${downloaded ? '[saved]' : '[skip] '} ${hotel.slug}/${i + 1}.jpg`);
  }

  return paths;
}

async function main() {
  fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  fs.mkdirSync(path.dirname(DATA_OUT), { recursive: true });
  console.log(`Fetching images for ${hotels.length} hotels...\n`);

  const result = {};
  for (const hotel of hotels) {
    try {
      result[hotel.slug] = await fetchHotelImages(hotel);
    } catch (err) {
      console.error(`  [error] ${hotel.slug} — ${err.message}`);
      result[hotel.slug] = [];
    }
    console.log();
  }

  // Write TypeScript data file
  const lines = ['// Auto-generated by scripts/fetch-hotel-images.mjs', 'const hotelImages: Record<string, string[]> = {'];
  for (const [slug, paths] of Object.entries(result)) {
    lines.push(`  '${slug}': [`);
    for (const p of paths) lines.push(`    '${p}',`);
    lines.push(`  ],`);
  }
  lines.push('};', '', 'export default hotelImages;', '');
  fs.writeFileSync(DATA_OUT, lines.join('\n'));
  console.log(`\nWrote ${DATA_OUT}`);

  const total = Object.values(result).reduce((n, a) => n + a.length, 0);
  console.log(`Done: ${total} photos across ${hotels.length} hotels.`);
}

main();
